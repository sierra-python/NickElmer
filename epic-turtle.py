import turtle


while True:
    my_turtle = turtle.Turtle()   # create a turtle
    my_turtle.color("blue")      # set the color
    my_turtle.speed(1)
    my_turtle.forward(50)
    my_turtle.color("green")      # set the color
    my_turtle.up()
    my_turtle.forward(50)
    my_turtle.color("red")      # set the
    my_turtle.right(90)
    my_turtle.forward(50)
    my_turtle.color("orange")      # set the color
    my_turtle.right(90)
    my_turtle.forward(50)
    my_turtle.color("yellow")      # set the color
    my_turtle.down()
    my_turtle.color("pink")      # set the color
